const loc = getLocCode();

let $title = document.querySelector('head title');
let $textList = document.querySelector('.text-list ul');

const info = COPY_WRITE[loc] || COPY_WRITE['en'];
$title.innerHTML = info.title;
$textList.innerHTML = getContentHtml(info.content);

function getLocCode() {
    let loc;
    if (navigator.appName == 'Netscape')
        loc = navigator.language.toLowerCase();
    else
        loc = navigator.browserLanguage.toLowerCase();
    if (/zh-cn/.test(loc)) {
        return loc
    } else if (/zh/.test(loc)) {
        return 'zh-TW'
    } else {
        return 'en'
    }
}

function getContentHtml(text) {
    let html = '';
    text.forEach((value) => {
        const itemText = Array.isArray(value.text) ?
            value.text.map((item) => {
                return `<p>${item}</p>`
            }).join('')
            : `<p>${value.text}</p>`;
        html += `<li>
                <div>
                    <div class="item-title">
                        <div class="icon">Q</div><strong>${value.title}</strong>
                    </div>
                    <div class="item-text">
                        ${itemText}
                    </div>
                </div>
            </li>`
    });
    return html;
}


function setFontSize() {
    let winWidth = window.innerWidth;
    let defaultWidth = 750 / 2;
    let itemSize = 20;
    let fontSize = winWidth / defaultWidth * itemSize / 16 * 100;
    document.querySelector('html').style.fontSize = `${fontSize}%`;
}